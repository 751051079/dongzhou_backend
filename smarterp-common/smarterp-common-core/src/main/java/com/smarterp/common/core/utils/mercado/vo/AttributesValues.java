package com.smarterp.common.core.utils.mercado.vo;

public class AttributesValues {

    private String id;

    private String name;

    private AttributesValueStruct struct;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public AttributesValueStruct getStruct() {
        return struct;
    }

    public void setStruct(AttributesValueStruct struct) {
        this.struct = struct;
    }
}
